
public class Loops {
	
	void getForLoop()
	{
		for(int i=1;i<=10;i++)
		{
			System.out.print(i+" ");
		}
	}
	void getWhileLoop()
	{
		int i=1;
		while(i<=10)
		{
			System.out.print(i+" ");
			i++;
		}
	}
	void getDoWhileLoop()
	{
		int i=1;
		do
		{
			System.out.print(i+" ");
			i++;
		}
		while(i<=10); 
							
	}

}
